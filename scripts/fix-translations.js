// Function to read a TypeScript translation file
function readTranslationFile(filePath) {
  const content = fs.readFileSync(filePath, "utf8");
  return content;
}

// Function to add missing keys to a translation file
function addMissingKeys(filePath, missingKeys) {
  let content = readTranslationFile(filePath);

  // Add loadingStage and loadingSubStage keys after mayTakeMoment
  if (missingKeys.common) {
    const loadingKeysStr = Object.entries(missingKeys.common)
      .map(([key, value]) => `    ${key}: "${value}",`)
      .join("\n");

    // Find the common section and add after mayTakeMoment
    content = content.replace(
      /(mayTakeMoment:.*?,\n)/,
      `$1    // Multi-stage loading messages\n${loadingKeysStr}\n`
    );
  }

  // Add settings keys
  if (missingKeys.settings) {
    // Add accessibilitySettings after appSettings
    if (missingKeys.settings.accessibilitySettings) {
      content = content.replace(
        /(appSettings:.*?,\n)/,
        `$1    accessibilitySettings: "${missingKeys.settings.accessibilitySettings}",\n`
      );
    }

    // Add reduceMotion, reduceMotionDescription, hapticFeedback, hapticFeedbackDescription
    const settingsKeysToAdd = [
      "reduceMotion",
      "reduceMotionDescription",
      "hapticFeedback",
      "hapticFeedbackDescription",
    ];
    settingsKeysToAdd.forEach((key) => {
      if (missingKeys.settings[key] && !content.includes(`${key}:`)) {
        // Add after invalidAppStoreUrl
        content = content.replace(
          /(invalidAppStoreUrl:.*?,\n)/,
          `$1    ${key}: "${missingKeys.settings[key]}",\n`
        );
      }
    });
  }

  // Add multiStep section before screenshots
  if (missingKeys.multiStep) {
    const multiStepStr = `  multiStep: {
    // Navigation
    next: "${missingKeys.multiStep.next}",
    back: "${missingKeys.multiStep.back}",
    skip: "${missingKeys.multiStep.skip}",
    done: "${missingKeys.multiStep.done}",
    startAnalysis: "${missingKeys.multiStep.startAnalysis}",

    // Progress indicator
    progress: "${missingKeys.multiStep.progress}",
    stepOf: "${missingKeys.multiStep.stepOf}",

    // Welcome Step
    welcomeTitle: "${missingKeys.multiStep.welcomeTitle}",
    welcomeSubtitle:
      "${missingKeys.multiStep.welcomeSubtitle}",
    feature1Title: "${missingKeys.multiStep.feature1Title}",
    feature1Desc:
      "${missingKeys.multiStep.feature1Desc}",
    feature2Title: "${missingKeys.multiStep.feature2Title}",
    feature2Desc:
      "${missingKeys.multiStep.feature2Desc}",
    feature3Title: "${missingKeys.multiStep.feature3Title}",
    feature3Desc: "${missingKeys.multiStep.feature3Desc}",
    privacyNote:
      "${missingKeys.multiStep.privacyNote}",

    // Preview Step
    previewTitle: "${missingKeys.multiStep.previewTitle}",
    previewSubtitle: "${missingKeys.multiStep.previewSubtitle}",
    whatHappensNext: "${missingKeys.multiStep.whatHappensNext}",
    analysisInfo1: "${missingKeys.multiStep.analysisInfo1}",
    analysisInfo2: "${missingKeys.multiStep.analysisInfo2}",
    analysisInfo3: "${missingKeys.multiStep.analysisInfo3}",

    // Analysis Step
    analysisTitle: "${missingKeys.multiStep.analysisTitle}",
    analysisSubtitle: "${missingKeys.multiStep.analysisSubtitle}",

    // Results Step
    resultsTitle: "${missingKeys.multiStep.resultsTitle}",
    resultsSubtitle: "${missingKeys.multiStep.resultsSubtitle}",
  },
`;

    // Add before screenshots section
    content = content.replace(/(\n  screenshots: \{)/, `${multiStepStr}$1`);
  }

  fs.writeFileSync(filePath, content, "utf8");
  console.log(`Updated: ${filePath}`);
}

// Main execution
const translationFiles = fs
  .readdirSync(TRANSLATIONS_DIR)
  .filter((file) => file.endsWith(".ts") && file !== "index.ts");

translationFiles.forEach((file) => {
  const locale = file.replace(".ts", "");
  const filePath = path.join(TRANSLATIONS_DIR, file);

  // Skip en.ts and es.ts as they already have all keys
  if (locale === "en" || locale === "es") {
    console.log(`Skipping ${file} (already complete)`);
    return;
  }

  if (missingKeysByLocale[locale]) {
    addMissingKeys(filePath, missingKeysByLocale[locale]);
  } else {
    console.log(`Warning: No missing keys defined for ${locale}`);
  }
});

console.log("\nTranslation fix complete!");
